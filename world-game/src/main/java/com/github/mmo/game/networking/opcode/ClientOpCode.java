        if (opCodeIntMap == null) {
            synchronized (ClientOpCode.class) {
                if (opCodeIntMap == null) {
                    ClientOpCode[] values = ClientOpCode.values();
                    opCodeIntMap = new IntMap<>(values.length, 1f);
                    for (var e : values) {
                        opCodeIntMap.put(e.value, e);
                    }
                }
            }
        }
        return opCodeIntMap.get(value);
